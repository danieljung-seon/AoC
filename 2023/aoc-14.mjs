function step(map) {
  let copy = JSON.parse(JSON.stringify(map));

  for (let y = 0; y < map.length; y++) {
    const row = map[y];

    for (let x = 0; x < row.length; x++) {
      const seat = row[x];
      if (seat === "O") {
        const up = map[y - 1] && map[y - 1][x];
        if (up === ".") {
          copy[y - 1][x] = "O";
          copy[y][x] = ".";
        }
      }
    }
  }

  return copy;
}

function solve(input) {
  let map = input
    .split("\n")
    .map((line) => line.split(""));

  for (let s = 0; s < map.length; s++) {
    map = step(map);
  }

  return map
    .map((v, i, arr) => v.filter(v2 => v2 === "O").length * (arr.length - i))
    .reduce((a, b) => a + b, 0);
}

